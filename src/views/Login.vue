<script>
import axios from "axios";
export default {
  data() {
    return {
      name: "",
      password: "",
      status: "",
      loader: true
    };
  },
  methods: {
    async login() {
      this.status = "";

      if(this.name.lenght < 3 || this.password < 3) {
          this.status = "Name or Password shorts"
          return
      }
      this.loader = false


      let dados = {
        name: this.name,
        password: this.password,
      };
      await axios.post("http://localhost:8888/login", dados).then((e) => {
        this.status = e.data.message;
        e.data.token ? localStorage.setItem('token', e.data.token) : null
        e.data.token ? this.$store.state.token = e.data.token : null
        e.data.user ? this.$store.state.userName = e.data.user : null
        e.data.token ? this.$router.push('/') : null
        
        
      });

      this.loader = true
    },
  },
};
</script>

